const DIGRAPH_MAP: Record<string, string> = {
  sh: 'ش', ch: 'تش', ph: 'ف', gh: 'غ', kh: 'خ',
  dj: 'ج', tz: 'تز', ts: 'تس', qu: 'ك', ck: 'ك',
  th: 'ت', wh: 'و',
  oo: 'و', ou: 'و', au: 'او', aw: 'او',
  ai: 'اي', ay: 'اي', ei: 'ي', ey: 'اي',
  ee: 'ي', ie: 'ي', oi: 'وي', ea: 'ي', oa: 'وا', ue: 'و',
  ll: 'ل', rr: 'ر', ss: 'س', tt: 'ت', nn: 'ن',
  mm: 'م', bb: 'ب', ff: 'ف', pp: 'ب', dd: 'د', cc: 'ك',
};

const SINGLE_MAP: Record<string, string> = {
  a: 'ا', b: 'ب', c: 'ك', d: 'د', e: 'ي',
  f: 'ف', g: 'غ', h: 'ه', i: 'ي', j: 'ج',
  k: 'ك', l: 'ل', m: 'م', n: 'ن', o: 'و',
  p: 'ب', q: 'ك', r: 'ر', s: 'س', t: 'ت',
  u: 'و', v: 'ف', w: 'و', x: 'كس', y: 'ي', z: 'ز',
};

function transliterateWord(word: string): string {
  const s = stripAccents(word);
  if (!s || /^[a-z]\.$/.test(s)) return '';
  let result = '';
  let i = 0;
  while (i < s.length) {
    const two = s[i] + (s[i + 1] ?? '');
    const digraph = DIGRAPH_MAP[two];
    if (digraph !== undefined) {
      result += digraph;
      i += 2;
    } else {
      result += SINGLE_MAP[s[i]] ?? '';
      i++;
    }
  }
  return result;
}

function transliterateToArabic(englishName: string): string {
  const words = englishName.trim().split(/[\s\-]+/).filter(Boolean);
  const parts = words.map(transliterateWord).filter(Boolean);
  return parts.length > 0 ? parts.join(' ') : englishName;
}

