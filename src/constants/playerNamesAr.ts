const PLAYER_NAMES_AR_LOWER: Record<string, string> = Object.fromEntries(
  Object.entries(PLAYER_NAMES_AR).map(([k, v]) => [k.toLowerCase(), v])
);

function stripAccents(s: string): string {
  return s.normalize('NFD').replace(/[̀-ͯ]/g, '').toLowerCase();
}

const PLAYER_NAMES_AR_STRIPPED: Record<string, string> = Object.fromEntries(
  Object.entries(PLAYER_NAMES_AR).map(([k, v]) => [stripAccents(k), v])
);

// Last-name index: "messi" → "ليونيل ميسي"
// Handles abbreviated API names like "L. Messi" or just "Messi"
const PLAYER_NAMES_AR_LASTNAME: Record<string, string> = {};
for (const [key, value] of Object.entries(PLAYER_NAMES_AR)) {
  const words = stripAccents(key).trim().split(/\s+/);
  const last = words[words.length - 1];
  if (last && last.length > 2 && !PLAYER_NAMES_AR_LASTNAME[last]) {
    PLAYER_NAMES_AR_LASTNAME[last] = value;
  }
}

// Core last-name index: strips "al-" / "abu-" prefixes so "al-hamadi" and
// "hamadi" (from API "Al Hamadi") both resolve to the same Arabic name.
const PLAYER_NAMES_AR_CORE: Record<string, string> = {};
for (const [key, value] of Object.entries(PLAYER_NAMES_AR)) {
  const words = stripAccents(key).trim().split(/\s+/);
  const last = words[words.length - 1];
  const core = last.replace(/^(al|abu|bin|bani|ibn)-/, '');
  if (core && core.length > 3 && core !== last && !PLAYER_NAMES_AR_CORE[core]) {
    PLAYER_NAMES_AR_CORE[core] = value;
  }
}

