export function getPlayerNameAr(englishName: string): string {
  if (!englishName) return englishName;

  // Tier 1-3: exact / lowercase / accent-stripped full-name lookup
  const direct =
    PLAYER_NAMES_AR[englishName] ??
    PLAYER_NAMES_AR_LOWER[englishName.toLowerCase()] ??
    PLAYER_NAMES_AR_STRIPPED[stripAccents(englishName)];
  if (direct) return direct;

  // Tier 4: last-name lookup — handles "Messi" or "L. Messi" → "ليونيل ميسي"
  const inputWords = englishName.trim().split(/\s+/);
  const lastWord = stripAccents(inputWords[inputWords.length - 1]);
  if (lastWord.length > 2) {
    const byLast = PLAYER_NAMES_AR_LASTNAME[lastWord];
    if (byLast) return byLast;
  }

  // Tier 4b: core last-name — "Al Hamadi" (space) matches "Al-Hamadi" (hyphen)
  // by stripping the al-/abu- prefix and looking up the bare root word
  if (lastWord.length > 3) {
    const coreWord = lastWord.replace(/^(al|abu|bin|bani|ibn)-/, '');
    if (coreWord !== lastWord) {
      const byCore = PLAYER_NAMES_AR_CORE[coreWord];
      if (byCore) return byCore;
    }
    // Also try the last word itself as a core key (handles "Al Hamadi" → "hamadi")
    const byCore = PLAYER_NAMES_AR_CORE[lastWord];
    if (byCore) return byCore;
  }

  // Tier 4c: abbreviated-name bridge
  // Stats API returns "Saleh Al-Shehri" but dict has "S. Al-Shehri" (from squad API).
  // Build abbreviated form and retry tiers 1-3.
  if (inputWords.length >= 2 && !/\.$/.test(inputWords[0])) {
    const abbrev = inputWords[0][0].toUpperCase() + '. ' + inputWords.slice(1).join(' ');
    const byAbbrev =
      PLAYER_NAMES_AR[abbrev] ??
      PLAYER_NAMES_AR_LOWER[abbrev.toLowerCase()] ??
      PLAYER_NAMES_AR_STRIPPED[stripAccents(abbrev)];
    if (byAbbrev) return byAbbrev;

    // Also try with last two words abbreviated: "Ahmed El Yamiq" → "A. El Yamiq"
    // and without the middle name: "Mohammed Khalil Ahmed" → "M. Ahmed"
    if (inputWords.length >= 3) {
      const abbrevLast = inputWords[0][0].toUpperCase() + '. ' + inputWords[inputWords.length - 1];
      const byAbbrevLast =
        PLAYER_NAMES_AR[abbrevLast] ??
        PLAYER_NAMES_AR_LOWER[abbrevLast.toLowerCase()] ??
        PLAYER_NAMES_AR_STRIPPED[stripAccents(abbrevLast)];
      if (byAbbrevLast) return byAbbrevLast;
    }
  }

  // Tier 5: phonetic transliteration — covers any player not in the dictionary
  return transliterateToArabic(englishName);
}
